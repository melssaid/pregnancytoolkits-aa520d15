const fallback = <T,>(map: Partial<Record<ArticleTag, T>>, tag: ArticleTag, defaultValue: T): T =>
  map[tag] ?? defaultValue;

const joinList = (items: string[], lang: Lang) => {
  if (!items.length) return "";
  if (items.length === 1) return items[0];
  const last = items[items.length - 1];
  const head = items.slice(0, -1).join(", ");
  const conjunctions: Record<Lang, string> = {
    en: " and ",
    de: " und ",
    fr: " et ",
    es: " y ",
    tr: " ve ",
    pt: " e ",
  };
  return `${head}${conjunctions[lang]}${last}`;
};

