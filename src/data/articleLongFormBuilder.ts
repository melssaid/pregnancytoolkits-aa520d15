export const buildLongFormSections = (
  lang: SupportedArticleLanguage,
  input: BuilderInput,
): Section[] => {
  if (lang === "ar") return [];
  const langKey = lang as Lang;
  const heads = headings[langKey];
  const builders = composers[langKey];
  return heads.map((heading, idx) => ({ heading, body: builders[idx](input) }));
};
